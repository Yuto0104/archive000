//=============================================================================
// 波メッシュクラス
// Author : 唐﨑結斗
// 概要 : 波メッシュ生成を行うクラス
//=============================================================================
class CWaveMesh : public CMesh3D
{
public:
	//--------------------------------------------------------------------
	// 静的メンバ関数
	//--------------------------------------------------------------------
	static CWaveMesh *Create(void);				// スフィアの生成

	//--------------------------------------------------------------------
	// コンストラクタとデストラクタ
	//--------------------------------------------------------------------
	CWaveMesh();
	~CWaveMesh();

	//--------------------------------------------------------------------
	// メンバ関数
	//--------------------------------------------------------------------
	void Update() override;											// 更新
	void SetWave(const float fAddWave, const float fAmplitude);		// 波の設定

private:
	//--------------------------------------------------------------------
	// メンバ関数
	//--------------------------------------------------------------------
	void SetVtx() override;			// 頂点座標などの設定
	void Wave();					// 波

	//--------------------------------------------------------------------
	// メンバ変数
	//--------------------------------------------------------------------
	float	*m_fVtxHeight;		// 頂点の高さ
	float	m_fAddWave;			// 波の追加分の大きさ
	float	m_fAmplitude;		// 振幅
};

#endif
